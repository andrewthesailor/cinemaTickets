CREATE SEQUENCE S_SCREENING_ID START WITH 1 INCREMENT BY 50;
CREATE TABLE T_SCREENINGS
(
    SCREENING_ID NUMERIC PRIMARY KEY,
    SCREENING_ROOM_ID  NUMERIC(10,0) CONSTRAINT FK_SCREENING_ROOM REFERENCES T_ROOMS(ROOM_ID),
    SCREENING_TITLE VARCHAR(40) NOT NULL,
    SCREENING_START_HOUR TIMESTAMP NOT NULL
);

COMMENT ON COLUMN T_SCREENINGS.SCREENING_ID IS 'ID, GENERATED BY S_SCREENING_ID SEQUENCE';
COMMENT ON COLUMN T_SCREENINGS.SCREENING_ROOM_ID IS 'SCREENING ROOM ID, FOREIGN KEY TO T_ROOMS';
COMMENT ON COLUMN T_SCREENINGS.SCREENING_TITLE IS 'SCREENING TITLE, MAX LENGTH 40';
COMMENT ON COLUMN T_SCREENINGS.SCREENING_START_HOUR IS 'SCREENING START HOUR';
